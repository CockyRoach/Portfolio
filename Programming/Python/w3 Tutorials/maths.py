import math #importing the math module

#min, max
x = min(5, 10, 25)
y = max(5, 10, 25)

print(x)
print(y)

#absolute value
x = abs(-7.25)

print(x)

#power method, exponent
x = pow(4, 3)

print(x)

#squareroot
x = math.sqrt(64)

print(x)

#ceil and floor
x = math.ceil(1.4)
y = math.floor(1.4)

print(x) # returns 2
print(y) # returns 1

#pi value
x = math.pi

print(x)

